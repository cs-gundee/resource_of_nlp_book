print(s3)
# Түлхэж өгөхгүй юм бол бодол өөрөө биелэгдэнэ гэж үгүй

lyric = "нэ бол миний хатуу төлөв, юу ч намайг дийлэхгүй Чиглэж ирсэн хатуу чулуу бут үсрэн үйрэх вий"
wordToFind = "атуу"

# ‘хатуу’ үгийг эхнээс нь хайх
i = lyric.find(wordToFind)
print(i)
# Гаралт: 14

# Олдсон 14 дэх индексээс хойших 11 тэмдэгтийг тасдаж авах
print(lyric[i:i+11])
# Гаралт: хатуу төлөв

# ‘хатуу’ үгийг сүүлээс нь хайх
j = lyric.rfind(wordToFind)
print(j)
# Гаралт: 62

# Олдсон 62 дэх индексээс хойших 11 тэмдэгтийг гаргах
print(lyric[j:j+11])
# Гаралт: хатуу чулуу
