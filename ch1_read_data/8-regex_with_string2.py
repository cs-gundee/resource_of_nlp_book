s3 = s1 + s2
print(s3)
# Гаралт: Түлхэж өгөхгүй юм болбодол өөрөө биелэгдэнэ гэж үгүй

s3 = s1 + "" + s2
print(s3)
# Гаралт: Түлхэж өгөхгүй юм бол бодол өөрөө биелэгдэнэ гэж үгүй

s1 = "үлхэж өгөхгүй юм"
s2 = "одол өөрөө биелэгдэнэ гэж үгүй"

# f-string ашиглан тэмдэгт мөр залгах
