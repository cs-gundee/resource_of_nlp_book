str_lyric = "өрөөдлөө бүтэн биелэгдтэл нь сайн гүй Үүний төлөө бидэнд дий*эх ёстой дайн бий"

# Тэмдэгт мөрөөс эхний тэмдэгтийг авах
print(str_lyric[0])
# Гаралт: М

# Тэмдэгт мөрөөс 16-аас 26-р индекс хүртэлх хэсгийг тасдан авах
print(str_lyric[16:26])
# Гаралт: биелэгдтэл

# Тэмдэгт мөр доторх алдаатай бичсэн ‘дий*эх’ гэдгийг ‘дийлэх’ болгон солих
str_newlyric = str_lyric.replace("ий*эх", "ийлэх")
print(str_newlyric)
# Гаралт: Мөрөөдлөө бүтэн биелэгдтэл нь сайн гүй Үүний төлөө бидэнд дийлэх ёстой дайн бий

s1 = "үлхэж өгөхгүй юм бол"
